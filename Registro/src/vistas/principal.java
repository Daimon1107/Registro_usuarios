    void listar() {
        
        try {
            Object[] persona = new Object[9];
            model = (DefaultTableModel) TablaDatos.getModel();//en esta linea se crea el modelo de la tabla
            
            
            for (int i = 0; i < lista.size(); i++) {
                persona[0] = lista.get(i).getCedula();
                persona[1] = lista.get(i).getApellido();
                persona[2] = lista.get(i).getNombre();
                persona[3] = lista.get(i).getProvincia();
                persona[4] = lista.get(i).getCiudad();
                persona[5] = lista.get(i).getFechaNac();
                persona[6] = lista.get(i).getNacionalidad();
                persona[7] = lista.get(i).getSexo();
                persona[8] = lista.get(i).getEstadoCivil();
                
                model.addRow(persona);
                
            }
            TablaDatos.setModel(model);

        } catch (Exception e) {
            System.out.println("Error "+e.getMessage());
        }

    }
    
    
    void limpiarTabla(DefaultTableModel model) {
        for (int i = 0; i <= TablaDatos.getRowCount(); i++) {
            model.removeRow(i);
            i = i - 1;
        }

    }
    
