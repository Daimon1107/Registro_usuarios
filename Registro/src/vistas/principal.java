    String codigo(String d1,String d2){
        Random random = new Random();
        String d3 = String.valueOf(random.nextInt(5 + 0) + 0);
        String d4 = String.valueOf(random.nextInt(9 + 0) + 0);
        String d5 = String.valueOf(random.nextInt(9 + 0) + 0);
        String d6 = String.valueOf(random.nextInt(9 + 0) + 0);
        String d7 = String.valueOf(random.nextInt(9 + 0) + 0);
        String d8 = String.valueOf(random.nextInt(9 + 0) + 0);
        String d9 = String.valueOf(random.nextInt(9 + 0) + 0);
        String cedula=d1+"-"+d2+"-"+d3+"-"+d4+"-"+d5+"-"+d6+"-"+d7+"-"+d8+"-"+d9;
        String split[]=cedula.split("-");
        int val,par=0,impar=0;
        for (int i = 0; i < split.length; i++) {
            val=Integer.parseInt(split[i]);
            if(i%2==0){
                val=(val*2);
                if(val>=10){
                    val=val-9;
                    par+=val;
                }
                else{
                    par+=val;
                }
            }
            else{
                impar+=(val*1);
            }
            
        }
        int total=par+impar;
        String d10="";
        if(total>10 && total<21){
            d10=String.valueOf(20-total);
        }
        else if(total>20 && total<31){
            d10=String.valueOf(30-total);
        }else if(total>30 && total<41){
            d10=String.valueOf(40-total);
        }else if(total>40 && total<51){
            d10=String.valueOf(50-total);
        }else if(total>50 && total<61){
            d10=String.valueOf(60-total);
        }else if(total>60 && total<71){
            d10=String.valueOf(70-total);
        }else if(total>70 && total<81){
            d10=String.valueOf(80-total);
        }else if(total>80 && total<91){
            d10=String.valueOf(90-total);
        }else if(total>90 && total<101){
            d10=String.valueOf(100-total);
        }
            
        
        
        
        return d1+d2+d3+d4+d5+d6+d7+d8+d9+d10;
        
        
    }
    
