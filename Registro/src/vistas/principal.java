    boolean soloLetras(String dato){
        boolean entradaNumerica = true;
        int c=0;
        for (int i=0;i< dato.length(); i++) {
            int l=dato.charAt(i);
            if ((l>96 && l<123) || (l==32) || (l>64 && l<91)) {
                c++;
            } 
        }
        
        if(c!=dato.length()){
            entradaNumerica = false;
        }
        return entradaNumerica;

        }
    
    
   
    void Agregar() {
        try {
            generadorCedula();
            txtCedula.setText(cedula);
            cedula=txtCedula.getText();
            String nombre=txtNombres.getText();
            String apellido=txtApellidos.getText();
            String prov=cmbProvincia.getSelectedItem().toString();
            String cant=cmbCanton.getSelectedItem().toString();
            String edad=spnFecha.getValue().toString();
            String nacionalidad=txtNacionalidad.getText();
            String sexo;
            if(rbtnHombre.isSelected()){
                sexo="Hombre";
            }
            else{
                sexo="Mujer";
            }
            String estadC=cmbEstadoC.getSelectedItem().toString();
            
            if(!soloLetras(nombre)){
                JOptionPane.showMessageDialog(rootPane, "Ingrese solo letras para el nombre");
            } else if(!soloLetras(apellido)){
                JOptionPane.showMessageDialog(rootPane, "Ingrese solo letras para el apellido");
            } else if(cmbProvincia.getSelectedIndex()==0){
                JOptionPane.showMessageDialog(rootPane, "Escoja una Provincia");
            } else if(cmbCanton.getSelectedIndex()==0){
                JOptionPane.showMessageDialog(rootPane, "Escoja un Canton");
            } else if(edad==null){
                JOptionPane.showMessageDialog(rootPane, "Asigne una edad");
            } else if(!soloLetras(nacionalidad)){
                JOptionPane.showMessageDialog(rootPane, "Ingrese solo letras para la nacionalidad");
            } else if(!rbtnHombre.isSelected() && !rbtnMujer.isSelected()){
                JOptionPane.showMessageDialog(rootPane, "Escoja un sexo");
            } else if(cmbEstadoC.getSelectedIndex()==0){
                JOptionPane.showMessageDialog(rootPane, "Escoja un Estado Civil");
            }else{
                persona p=new persona(cedula,nombre,apellido, prov, cant, edad, nacionalidad, sexo, estadC);
                lista.add(p);
                JOptionPane.showMessageDialog(rootPane, "Datos Ingresados Correctamente");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error "+e.getMessage());
        }
        
        
    }
